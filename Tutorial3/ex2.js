colors = ["red", "green", "blue", "yellow"];

// Remove the last element using pop() and store it in a variable
removedColor = colors.pop();

// Remove the first element using shift() and store it in a variable
firstColor = colors.shift();

// Print the modified array and the removed elements
console.log("Modified colors:", colors);
console.log("Removed color from the end:", removedColor);
console.log("Removed color from the beginning:", firstColor);
