function calculateArea(width, height) {
    
  
    // Calculate the area
    const area = width * height;
  
    // Return the calculated area
    return area;
  }
  
  // Test the function with different values
  width1 = 5;
  height1 = 3;
  area1 = calculateArea(width1, height1);
  console.log("Area of rectangle 1:", area1);  // Output: Area of rectangle 1: 15
  
  width2 = 10;
  height2 = 2;
  area2 = calculateArea(width2, height2);
  console.log("Area of rectangle 2:", area2);  // Output: Area of rectangle 2: 20
  